-- setting up clipboard — use built-in OSC 52 so yank reaches host clipboard
-- (must be set before 'clipboard = unnamedplus')
local osc52 = require 'vim.ui.clipboard.osc52'
vim.g.clipboard = {
  name = 'OSC 52',
  copy = {
    ['+'] = osc52.copy '+',
    ['*'] = osc52.copy '*',
  },
  paste = {
    ['+'] = osc52.paste '+',
    ['*'] = osc52.paste '*',
  },
}
vim.o.clipboard = 'unnamedplus'
