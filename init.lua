-- 1. Define the folding capabilities
local capabilities = vim.lsp.protocol.make_client_capabilities()
capabilities.textDocument.foldingRange = {
  dynamicRegistration = false,
  lineFoldingOnly = true,
}
capabilities.workspace.didChangeWatchedFiles = {
  dynamicRegistration = true,
}

-- 2. Use the new Neovim 0.11+ configuration style
-- This replaces require('lspconfig').clangd.setup
vim.lsp.config('clangd', {
  cmd = {
    'clangd',
    '--background-index', -- index in background, persist to disk
    '--clang-tidy', -- enable clang-tidy diagnostics
    '--header-insertion=never', -- don't auto-insert headers (noisy in MariaDB)
    '--completion-style=detailed', -- show full signatures in completion
    '--function-arg-placeholders', -- fill in argument names on completion
    '--pch-storage=memory', -- faster preamble (uses more RAM)
    '-j=4', -- background indexing threads
    '--limit-results=100', -- cap completion results
  },
  capabilities = capabilities,
})

-- 3. Enable the folding engine
